cities = ["Katni", "Jabalpur", "Rewa", "Delhi", "Mumbai"]
students = ["Ankit", "Anand", "Chetan", "Pravesh"]

def findlength(list):
    return len(list)

lengthofcities = findlength(cities)
lengthofstudents = findlength(students)

print("Length of cities list:",lengthofcities)
print("Length of students list:",lengthofstudents)